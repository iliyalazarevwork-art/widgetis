function renderBody(body: string) {
  return body
    .split('\n\n')
    .filter(Boolean)
    .map((paragraph, index) => (
      <p key={`${index}-${paragraph.slice(0, 24)}`} className="simple-doc-page__text">
        {paragraph}
      </p>
    ))
}

